                    <h6>TESTING</h6>
                    <p>Time to test!!! I spoke to a few students and also some freelancers to test the prototype created in Adobe XD. I was able to get seven (7) persons to test, I had a mix of students - some have at some point used finance tool like cowryWise and banking apps while others had never interacted with such tool. From the test session, I noted parts of the app users had issues interacting with and also got direct feedback on some features.
One feature users had issues interacting with was the task manager on the homescreen which had a full calendar view of transactions, I noticed users were overwhelmed by this view so I decided to iterate on the design only showing a week view of and took off past events from the view to keep it clean and ensure users focus  and plan with current transactions.
After all these changes were made, another test session was scheduled for just five users, feedback from these sessions were key to iterating the interface design into the final solution.</p>
