                    <h5>CASE REVIEW</h5>
                    <p>The eTransact team wanted to build a new product providing solution to money management integrated as a new feature into their mobile banking application.
                    This product was going to be awesome, been that professionals, employers and students whom are active mobile phone users perform daily transactions on their phones digitally. 
                    Some of the features of this solution would be for the account inputs to be acquired through direct integration with the banking platform,
                    provision of all account transaction details for its customer.  </p>
                    <h5>CHALLENGE</h5>
                    <p>The assumption was that, there was a need for a platform that provides the user with their spending habits at a glance, and aid in better money management. 
                    The smartphone users running their daily transactions in Nigeria exceeds 146 million subscribers and over 90 million have access to internet services and data. The users cut across all sectors from the employees, students, employers and local business owners,  they all carry out expenses without having a proper balance of the inflow and outflow of funds,
                    thereby making it unrealistic to set and manage a budget. </p>
                    <h5>PROCESS</h5>
                    <p>

<ol><li>Discover - Usability review | Quantitative research | Research on similar apps</li>
<li>Understand - Document findings| Develop Personas </li>
<li> Ideate - Wireframe | High Fidelity </li>
<li>Review - User testing</li>
</ol>
</p>
                    <h5>Research</h5>
                    <p>The stakeholders sent a brief and I had to study it intensively to better understand the goal of the budget management  tool. This helped me to better understand the features associated with this tool and why those features existed.
Next, I interviewed people, which consisted of a student, a business man and an employed mother, who use banking applications and digital applications to run their daily transactions to know how they found such tools useful, what features they interact with frequently and what features they would like to see. I also explored other similar applications to understand how they work and to ascertain how best to visually represent features on the mobile app. This  helped me grasp the current experience, draft personas and provided direction for the user flow. </p>
                   

                   
